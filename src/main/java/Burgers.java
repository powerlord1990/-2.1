public class Burgers {
    int buns;
    int cheese;
    int chickenCutlet;
    int cucumbers;
    int tomato;
    int beefCutlet;
    int salad;
    int onion;
    boolean ketchup;
    boolean mayonnaise;
    boolean mustard;

    int price;

    Burgers(int b, int ch, int cC, int c, int t, int bC, int s, int o, boolean k, boolean m, boolean mu, int p) {

        buns = b;
        cheese = ch;
        chickenCutlet = cC;
        cucumbers = c;
        tomato = t;
        beefCutlet = bC;
        salad = s;
        onion = o;
        ketchup = k;
        mayonnaise = m;
        mustard = mu;
        price = p;
    }
}
